
function mostrar(){
	document.getElementById('mostrarOcultar').style.display="block";
	document.getElementById('botonMostrar').style.display="none";	
	document.getElementById('botonOcultar').style.display="block";	
}

function ocultar(){
	document.getElementById('mostrarOcultar').style.display="none";
	document.getElementById('botonOcultar').style.display="none";
	document.getElementById('botonMostrar').style.display="block";	
		
}
